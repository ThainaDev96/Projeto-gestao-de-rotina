    private void bt_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pesquisarActionPerformed
        // TODO add your handling code here:
        try {
            String descricao = txt_descricao.getText();

            String driverName = "com.mysql.jdbc.Driver";

            Class.forName(driverName);

            // Configurando a nossa conex√£o com um banco de dados//
            String serverName = "204.216.145.129";    //caminho do servidor do BD

            String mydatabase = "progb2024";        //nome do seu banco de dados

            String url = "jdbc:mysql://" + serverName + "/" + mydatabase + "?useUnicode=true&characterEncoding=UTF8&autoReconnect=true&useSSL=false";

            String username = "progb2024";        //nome de um usu√°rio de seu BD      

            String password = "progb2024";      //sua senha de acesso

            Connection conn = DriverManager.getConnection(url, username, password);

            Statement stmt = conn.createStatement();

            ResultSet rs = stmt.executeQuery("SELECT * FROM thaina_categoria");//asterisco serve para pegar todas colunas do banco de dados , se quiser algo em especifico nao necessita do asterisco,escrever so o que quer ex id ou so descri√ß√£o
            //result set serve para fazer pesquisa , √© uma classe de banco de dados, "rs" √© nome da vari√°vel. executeQuery executa a pesquisa √© um metodo do result
            while (rs.next()) { //loop para exibir at√© o final da tabela. Vai o que est√° no banco de dados
                int idCategoria = rs.getInt("id");//tudo do banco de dados 
                descricao = rs.getString("descricao");

                System.out.println("ID= " + idCategoria + "   descricao= " + descricao);
            }
            conn.close();

        } catch (SQLException e) { //captura erro especifico do banco de dados: SLQLException . "e" significa nome da vari√°vel
            System.out.println(e.getLocalizedMessage());
        } catch (Exception erroGenerico) {
            System.out.println("erro generico = " + erroGenerico.getLocalizedMessage());
            System.out.println("Querido usu√°rio temos um erro " + erroGenerico.fillInStackTrace());//fillInStackTrace informa o tipo de erro
            JOptionPane.showMessageDialog(null, "Ocorreu um erro");

        }


    }//GEN-LAST:event_bt_pesquisarActionPerformed

